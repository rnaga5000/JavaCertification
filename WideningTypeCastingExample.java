package MyPack;
//it is also known as implicit type casting,it is done by java
public class WideningTypeCastingExample 
{
	public static void main(String args[])
	{
		int i=10;
		double d=i;
		System.out.println("Converting int into double "+d);
		long l=i;
		System.out.println("Converting int into log "+l);
		
	}
}
